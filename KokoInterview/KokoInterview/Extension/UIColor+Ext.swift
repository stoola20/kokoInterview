import UIKit

extension UIColor {
    static var greynishBrown: UIColor {
        return UIColor(red: 71/255, green: 71/255, blue: 71/255, alpha: 1)
    }

    static var brownGrey: UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1)
    }

    static var hotPink: UIColor {
        return UIColor(red: 236/255, green: 0/255, blue: 140/255, alpha: 1)
    }

    static var white252: UIColor {
        return UIColor(red: 252/255, green: 252/255, blue: 252/255, alpha: 1)
    }

    static var white239: UIColor {
        return UIColor(red: 239/255, green: 239/255, blue: 239/255, alpha: 1)
    }

    static var frogGreen: UIColor {
        return UIColor(red: 86/255, green: 179/255, blue: 11/255, alpha: 1)
    }

    static var booger: UIColor {
        return UIColor(red: 166/255, green: 204/255, blue: 66/255, alpha: 1)
    }
}
