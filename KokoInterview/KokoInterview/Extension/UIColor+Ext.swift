import UIKit

extension UIColor {
    static var greynishBrown: UIColor {
        return UIColor(red: 71, green: 71, blue: 71, alpha: 1)
    }

    static var brownGrey: UIColor {
        return UIColor(red: 153, green: 153, blue: 153, alpha: 1)
    }

    static var hotPink: UIColor {
        return UIColor(red: 236, green: 0, blue: 140, alpha: 1)
    }
}
